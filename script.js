// Perenual Plant Information API:
function getPlant() {
  requestUrl = `https://perenual.com/api/species-list?key=${plantApiKey}&q=${searchBar.value}`;
  try {
    fetch(requestUrl)
      .then(function (response) {
        if (!response.ok) {
          throw new Error("Network response error. Try again!");
        } else {
          return response.json();
        }
      })
      .then(function (data) {
        let plantData = data.data[0];
        plantImage.src =
          plantData.default_image.thumbnail === undefined
            ? "./assets/images/plant-image-placeholder.png"
            : plantData.default_image.thumbnail;
        plantName.innerHTML = plantData.common_name;
        scientificName.innerHTML = plantData.scientific_name;
        moreImgLink.classList.remove("pointer-events-none");
        moreImgLink.href = `https://www.google.com/images?q=${plantData.scientific_name}`;
        otherName.innerHTML =
          plantData.other_name[0] === undefined
            ? "no other name available"
            : `this plant is also known as ${plantData.other_name}`;
        wateringCycle.innerHTML = `Watering: ${plantData.watering}`;
        sunLight.innerHTML = `Sunlight: ${
          plantData.sunlight[0] === undefined
            ? "data not available"
            : plantData.sunlight[0]
        }`;
        plantShade.innerHTML = `Shade: ${
          plantData.sunlight[1] === undefined
            ? "data not available"
            : plantData.sunlight[1]
        }`;
        localStorage.setItem("recentSearch", plantData.common_name);
      });
  } catch (error) {
    console.log(error);
  }
}

